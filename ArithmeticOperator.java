package MainPackage.JavaBasics.operator;

public class ArithmeticOperator {
    public static void main(String[] args) {
        int a =100;
        int b =200;
        //=============== Addition=====================================================
        System.out.println(a+b);
        //=============== Subtraction==================================================
        System.out.println(b-a);
        //=============== Multiplication================================================
        System.out.println(a*b);
        //=============== Division=====================================================
        System.out.println(b/a);

    }
}
