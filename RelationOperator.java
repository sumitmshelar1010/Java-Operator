package MainPackage.JavaBasics.operator;

public class RelationOperator {
    // Answer of RelationOperator must be True or False
    public static void main(String[] args) {
        int a = 100;
        int b = 500;
        //Comparison
        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(a>=b);
        System.out.println(a<=b);

        //Equality
        System.out.println(a==b);
        System.out.println(a!=b);

    }
}
